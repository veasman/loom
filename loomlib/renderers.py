def render_tmux_theme(theme: Theme) -> str:
    return f"""# generated by loom

set -g status-style "bg={theme.bg_alt},fg={theme.fg}"

set -g pane-border-style "fg={theme.border_inactive}"
set -g pane-active-border-style "fg={theme.border_active}"

set -g message-style "bg={theme.accent},fg={theme.accent_fg}"

set -g status-left-style "bg={theme.bg_alt},fg={theme.accent}"
set -g status-right-style "bg={theme.bg_alt},fg={theme.fg_muted}"

set -g window-status-style "bg={theme.bg_alt},fg={theme.fg_muted}"
set -g window-status-current-style "bg={theme.bg_alt},fg={theme.accent}"

set -g mode-style "bg={theme.accent},fg={theme.accent_fg}"

set -g status-justify left
set -g status-left-length 30
set -g status-right-length 100
"""


