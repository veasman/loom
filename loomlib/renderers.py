def render_nvim_theme(theme: Theme) -> str:
    transparent = "true" if theme.nvim_transparent else "false"
    palette = theme.custom_palette()

    if palette:
        palette_lines = "\n".join([f'    {k} = "{v}",' for k, v in palette.items()])
        return f"""return {{
  colorscheme = "{theme.nvim_scheme}",
  variant = "{theme.nvim_variant}",
  transparent = {transparent},
  palette = {{
{palette_lines}
  }},
}}
"""
    return f"""return {{
  colorscheme = "{theme.nvim_scheme}",
  variant = "{theme.nvim_variant}",
  transparent = {transparent},
}}
"""


