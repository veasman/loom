def render_session_theme(theme: Theme) -> str:
    gtk_theme = "Adwaita-dark" if theme.ui_mode == "dark" else "Adwaita"
    return f"""# generated by loom
export GTK_THEME={gtk_theme}
"""


def render_gtk_settings(theme: Theme) -> str:
    gtk_theme = "Adwaita-dark" if theme.ui_mode == "dark" else "Adwaita"
    prefer_dark = "1" if theme.ui_mode == "dark" else "0"
    return f"""[Settings]
gtk-theme-name={gtk_theme}
gtk-icon-theme-name=Adwaita
gtk-application-prefer-dark-theme={prefer_dark}
"""
