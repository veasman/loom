def render_rofi_theme(theme: Theme) -> str:
    return f"""* {{
    bg: {theme.bg};
    bg-alt: {theme.bg_alt};
    fg: {theme.fg};
    fg-muted: {theme.fg_muted};
    accent: {theme.accent};
    accent-fg: {theme.accent_fg};
    border: {theme.border_active};
    selection-bg: {theme.selection_bg};
    selection-fg: {theme.selection_fg};
    font: "{theme.font_ui} {theme.font_size_ui}";
}}
"""


def render_nvim_theme(theme: Theme) -> str:
    transparent = "true" if theme.nvim_transparent else "false"
    return f"""return {{
  colorscheme = "{theme.nvim_scheme}",
  variant = "{theme.nvim_variant}",
  transparent = {transparent},
}}
"""


